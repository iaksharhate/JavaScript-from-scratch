console.log("Data Types in JS");

// JavaScript allows us to change variable type in runtime. 

// We can change the data type of variable in runtime,
// if we declare variable with let keyword.
// let we use to declare block scope variable.
// let variable can be updated but not redeclared.
let a = 369;
console.log("value of a " + a);

a = "Akshar";
console.log(a);

// We cannot change the data type of variable in runtime,
// if we declare variable with const keyword.
// const use to declare CONSTANTS in program.
// const variable can be updated but not redeclared.
// const variable has to be initiallize at the time of declaration.
const b = "India";
console.log(b);

